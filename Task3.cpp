#include<Iostream>
using namespace std;
int main() {
	//Task 3
	
	int number = 77; 
	
	number+=7; // add 7 to number
	cout<<"number+=7"<<number<<endl;
	
	number-=6; // subtract 6 from number
	cout<<"number-=7"<<number<<endl;
	
	number*=5;  //multiply 5 to the number 
	cout<<"number*=7"<<number<<endl;
	
	number/=4;  //divide the number by 4
	cout<<"number/=7"<<number<<endl;
	
	number%=3;  // take modulo 3 of the number
	cout<<"number%=7"<<number<<endl;
	
	cout<<"final value ="<<number<<endl;
	
}